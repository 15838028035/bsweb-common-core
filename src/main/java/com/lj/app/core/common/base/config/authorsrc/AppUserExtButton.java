package com.lj.app.core.common.base.config.authorsrc;

public class AppUserExtButton {

	private String key;

	private String name;

	private String code;

	private String url;

	private String multicount;

	private String beforeopen;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getMulticount() {
		return multicount;
	}

	public void setMulticount(String multicount) {
		this.multicount = multicount;
	}

	public String getBeforeopen() {
		return beforeopen;
	}

	public void setBeforeopen(String beforeopen) {
		this.beforeopen = beforeopen;
	}
}
